import java.util.*;

public class codingmafia5 {
    public static void main(String[] args) {
        int a = 10;
        int b = 30;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(b / a);
        System.out.println(b % a);
        System.out.println(Math.abs(-9.78));
        System.out.println(Math.min(20, 6));
        System.out.println(Math.min(9, 80));
        System.out.println(Math.ceil(9.78));
        System.out.println(Math.floor(9.78));
        System.out.println(Math.random());
        int r = 10 + (int) Math.random() * 90;
        System.out.println(r);
        System.out.println(Math.PI);
        System.out.println(Math.pow(2, 3));
        System.out.println(Math.sqrt(9));
        int e = 100;
        e++;
        System.out.println(e);
        ++e;
        System.out.println(e);

    }
}
